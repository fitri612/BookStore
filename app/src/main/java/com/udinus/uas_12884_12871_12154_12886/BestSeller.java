package com.udinus.uas_12884_12871_12154_12886;

public class BestSeller {
    int imagebest;
    String name;
    int price;

    public BestSeller(int imagebest, String name, int price) {
        this.imagebest = imagebest;
        this.name = name;
        this.price = price;
    }

    public int getImagebest() {
        return imagebest;
    }

    public String getName() {
        return name;
    }

    public int getPrice() {
        return price;
    }
}
